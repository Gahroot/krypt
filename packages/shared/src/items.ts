/**
 * Re-roll an owned item's potential tier + bonus stat lines using the public weighted table.
 * Returns a NEW ItemInstance — never mutates the original.
 *
 * The odds are PUBLIC, DETERMINISTIC, and identical to the initial drop roll:
 *   RARE 76.92% · EPIC 19.23% · UNIQUE 3.46% · LEGENDARY 0.385%
 *
 * The injectable RNG makes this fully testable and later drivable by Chainlink VRF.
 *
 * @param instance  The item to reroll (not mutated).
 * @param rng       RNG function returning [0,1). Defaults to Math.random; inject for tests/VRF.
 * @returns         A new ItemInstance with fresh potentialTier + potentialLines.
 */
export function rerollPotential(
  instance: ItemInstance,
  rng: () => number = Math.random,
): ItemInstance {
  const newTier = rollPotential(rng);
  const newLines = rollPotentialLines(newTier, rng);
  return {
    uid: instance.uid,
    defId: instance.defId,
    baseRank: instance.baseRank,
    potentialTier: newTier,
    potentialLines: newLines,
    ...(instance.enhancements ? { enhancements: instance.enhancements } : {}),
    ...(instance.bonusStats ? { bonusStats: instance.bonusStats } : {}),
  };
}

/** Human-readable one-line summary, e.g. "Legendary Iron Broadsword (Star-forged)". */
export function describeInstance(inst: ItemInstance): string {
  const def = getItemDef(inst.defId);
  const name = def ? def.name : inst.defId;
  return `${getPotentialTierInfo(inst.potentialTier).label} ${name} (${inst.baseRank})`;
}
