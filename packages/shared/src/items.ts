/**
 * Check whether a character can equip an item. Checks levelReq, classReq, and each stat
 * requirement (reqStr / reqDex / reqInt / reqLuk). Returns `{ ok: true }` if all pass,
 * or `{ ok: false, reason }` with the first failing requirement.
 */
export function canEquip(
  def: ItemDef,
  ctx: { level: number; stats: CharacterStats; archetype: ClassArchetype },
): { ok: boolean; reason?: string } {
  if (ctx.level < def.levelReq) {
    return { ok: false, reason: `Requires level ${def.levelReq}, have ${ctx.level}` };
  }
  if (def.classReq && !def.classReq.includes(ctx.archetype)) {
    return {
      ok: false,
      reason: `Restricted to ${def.classReq.join(", ")}, have ${ctx.archetype}`,
    };
  }
  if (def.reqStr !== undefined && ctx.stats.STR < def.reqStr) {
    return { ok: false, reason: `Requires ${def.reqStr} STR, have ${ctx.stats.STR}` };
  }
  if (def.reqDex !== undefined && ctx.stats.DEX < def.reqDex) {
    return { ok: false, reason: `Requires ${def.reqDex} DEX, have ${ctx.stats.DEX}` };
  }
  if (def.reqInt !== undefined && ctx.stats.INT < def.reqInt) {
    return { ok: false, reason: `Requires ${def.reqInt} INT, have ${ctx.stats.INT}` };
  }
  if (def.reqLuk !== undefined && ctx.stats.LUK < def.reqLuk) {
    return { ok: false, reason: `Requires ${def.reqLuk} LUK, have ${ctx.stats.LUK}` };
  }
  return { ok: true };
}

/**
 * Resolve which ring slot to use when equipping a ring item.
 * Rings can go in RING, RING_2, RING_3, or RING_4 — first empty wins,
 * falling back to RING if all four are occupied.
 */
export function resolveRingSlot(equipped: Map<string, string> | Record<string, string>): EquipSlot {
  const ringSlots = [EquipSlot.RING, EquipSlot.RING_2, EquipSlot.RING_3, EquipSlot.RING_4];
  const has =
    equipped instanceof Map ? (s: EquipSlot) => equipped.has(s) : (s: EquipSlot) => s in equipped;
  for (const s of ringSlots) {
    if (!has(s)) return s;
  }
  const fallback = ringSlots[0];
  if (fallback === undefined) {
    throw new Error("resolveRingSlot: no ring slots defined");
  }
  return fallback;
}
