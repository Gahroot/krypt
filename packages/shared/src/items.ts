export interface ItemDef {
  readonly id: string;
  readonly name: string;
  readonly slot: EquipSlot;
  /** Minimum character level to equip. */
  readonly levelReq: number;
  /** Which stat this item primarily boosts. */
  readonly primaryStat: PrimaryStat;
  /** Flat bonus to the primary stat before rank multiplier. */
  readonly baseStatBonus: number;
  /** Weapon attack (0 for armor). */
  readonly baseAttack: number;
  /** Weapon category — only set when slot is WEAPON. */
  readonly weaponType?: WeaponType;
  /** If set, only these class archetypes may equip the item. */
  readonly classReq?: readonly ClassArchetype[];
  /** Equipment set membership — items sharing a setId form a set with tiered bonuses. */
  readonly setId?: string;

  // ── Equip requirements (MapleStory-parity) ────────────────────────
  /** Minimum STR to equip. */
  readonly reqStr?: number;
  /** Minimum DEX to equip. */
  readonly reqDex?: number;
  /** Minimum INT to equip. */
  readonly reqInt?: number;
  /** Minimum LUK to equip. */
  readonly reqLuk?: number;

  // ── Defensive / utility stats ─────────────────────────────────────
  /** Physical defence bonus. */
  readonly wDef?: number;
  /** Magical defence bonus. */
  readonly mDef?: number;
  /** Speed bonus (100 = base). */
  readonly speed?: number;
  /** Jump bonus (100 = base). */
  readonly jump?: number;
  /** Flat HP bonus when equipped. */
  readonly hpBonus?: number;
  /** Flat MP bonus when equipped. */
  readonly mpBonus?: number;
  /** Ammo category consumed by this weapon (arrows, bullets, stars). Only set for ranged/thrown weapons. */
  readonly ammoType?: AmmoCategory;
}

/** A single rolled bonus-stat line from the Potential system. */
export interface PotentialLine {
  readonly stat: PrimaryStat | "HP" | "MP" | "ATK";
  /** Percent bonus, e.g. 9 = +9%. */
  readonly percent: number;
}

// ─── Bonus Stats (Flame system) ───────────────────────────────────────────

/** A single rolled bonus-stat line from the Flame system. Independent of Potential. */
export interface BonusStatLine {
  readonly stat: PrimaryStat | "HP" | "MP" | "ATK" | "WDEF" | "MDEF" | "SPEED" | "JUMP";
  /** Flat bonus value (not percent — flat additive). */
  readonly value: number;
  /** Flame tier: how strong this roll is (NORMAL → RARE → EPIC → UNIQUE → LEGENDARY). */
  readonly tier: "NORMAL" | "RARE" | "EPIC" | "UNIQUE" | "LEGENDARY";
}

/** Possible stats a flame line can roll. */
const FLAME_STATS: readonly BonusStatLine["stat"][] = [
  "STR",
  "DEX",
  "INT",
  "LUK",
  "HP",
  "MP",
  "ATK",
  "WDEF",
  "MDEF",
  "SPEED",
  "JUMP",
];

/** Flat value ranges per flame tier [min, max] (inclusive). */
const FLAME_TIER_RANGES: Record<BonusStatLine["tier"], readonly [number, number]> = {
  NORMAL: [1, 3],
  RARE: [3, 6],
  EPIC: [6, 10],
  UNIQUE: [10, 15],
  LEGENDARY: [15, 24],
} as const;

/** Weighted tier distribution for flame rolls (ascending rarity). */
const FLAME_TIER_TABLE: readonly {
  readonly tier: BonusStatLine["tier"];
  readonly weight: number;
}[] = [
  { tier: "NORMAL", weight: 1000 },
  { tier: "RARE", weight: 300 },
  { tier: "EPIC", weight: 60 },
  { tier: "UNIQUE", weight: 10 },
  { tier: "LEGENDARY", weight: 1 },
];

const TOTAL_FLAME_WEIGHT = FLAME_TIER_TABLE.reduce((s, t) => s + t.weight, 0);

/** Number of bonus-stat lines scaled by item level. */
export function flameLineCount(itemLevel: number): number {
  if (itemLevel >= 60) return 4;
  if (itemLevel >= 40) return 3;
  if (itemLevel >= 20) return 2;
  return 1;
}

/** Roll a single bonus-stat line using injectable RNG. Values scale with item level. */
export function rollBonusStatLine(
  itemLevel: number,
  rng: () => number = Math.random,
): BonusStatLine {
  // Pick tier via weighted table.
  let roll = rng() * TOTAL_FLAME_WEIGHT;
  let tier: BonusStatLine["tier"] = "NORMAL";
  for (const t of FLAME_TIER_TABLE) {
    if (roll < t.weight) {
      tier = t.tier;
      break;
    }
    roll -= t.weight;
  }

  // Pick stat.
  const stat = FLAME_STATS[Math.floor(rng() * FLAME_STATS.length)];
  if (stat === undefined) {
    throw new Error("rollFlame: FLAME_STATS is empty");
  }

  // Scale flat value by item level (higher level → higher base within the tier range).
  const [lo, hi] = FLAME_TIER_RANGES[tier];
  const levelScale = 0.5 + 0.5 * Math.min(itemLevel / 60, 1); // 0.5 at Lv1 → 1.0 at Lv60+
  const baseMin = Math.ceil(lo * levelScale);
  const baseMax = Math.floor(hi * levelScale);
  const value = baseMin + Math.floor(rng() * Math.max(1, baseMax - baseMin + 1));

  return { stat, value, tier };
}

/** Roll all bonus-stat lines for an item based on its level. */
export function rollBonusStats(
  itemLevel: number,
  rng: () => number = Math.random,
): BonusStatLine[] {
  const count = flameLineCount(itemLevel);
  const lines: BonusStatLine[] = [];
  for (let i = 0; i < count; i++) {
    lines.push(rollBonusStatLine(itemLevel, rng));
  }
  return lines;
}

/** Flame reroll cost in mesos. */
export const FLAME_REROLL_COST = 100;

/** Re-roll an item's bonus stats. Returns a NEW ItemInstance — never mutates. */
export function rerollBonusStats(
  instance: ItemInstance,
  itemLevel: number,
  rng: () => number = Math.random,
): ItemInstance {
  const newBonus = rollBonusStats(itemLevel, rng);
  return {
    uid: instance.uid,
    defId: instance.defId,
    baseRank: instance.baseRank,
    potentialTier: instance.potentialTier,
    potentialLines: instance.potentialLines,
    ...(instance.enhancements ? { enhancements: instance.enhancements } : {}),
    bonusStats: newBonus,
    ...(instance.stars !== undefined ? { stars: instance.stars } : {}),
  };
}

export type ScrollStatKind = PrimaryStat | "ATK" | "WDEF" | "MDEF" | "HP" | "MP" | "SPEED" | "JUMP";

/** A single successful scroll application stored on an ItemInstance. */
export interface EnhancementLine {
  readonly statKind: ScrollStatKind;
  readonly delta: number;
}

/** A concrete owned item: a def + its rolled quality. */
export interface ItemInstance {
  /** Unique instance id (assigned by the server on drop). */
  readonly uid: string;
  readonly defId: string;
  readonly baseRank: BaseRank;
  readonly potentialTier: PotentialTier;
  readonly potentialLines: readonly PotentialLine[];
  /** Cumulative stat bonuses from successful scroll applications. */
  readonly enhancements?: readonly EnhancementLine[];
  /** Flame bonus stats — independent flat stat lines rolled by item level. */
  readonly bonusStats?: readonly BonusStatLine[];
  /** Star Force level (0–15). Distinct from the 4-step base-rank ladder. */
  readonly stars?: number;
}

