/**
 * Resolve the attack type for a player. Checks the equipped weapon for a weaponType,
 * then falls back to the class archetype default.
 *
 * @param equipped Map of slot name → item uid (from the player's equipped schema)
 * @param inventoryLookup Function to look up an item's defId by uid
 * @param archetype ClassArchetype string (e.g. "ARCHER")
 */
export function resolveAttackType(
  equipped: Map<string, string> | Record<string, string>,
  inventoryLookup: (uid: string) => string | undefined,
  archetype: string,
): AttackType {
  const weaponUid =
    equipped instanceof Map ? equipped.get(EquipSlot.WEAPON) : equipped[EquipSlot.WEAPON];
  if (weaponUid) {
    const defId = inventoryLookup(weaponUid);
    if (defId) {
      const def = getItemDef(defId);
      if (def?.weaponType) {
        return WEAPON_TO_ATTACK[def.weaponType] ?? AttackType.MELEE;
      }
    }
  }
  return CLASS_DEFAULT_ATTACK[archetype] ?? AttackType.MELEE;
}

/**
 * Compute total bonus stats from equipped gear. Pure + deterministic — usable by both server
 * (combat) and client (stat preview).
 *
 * @param equipped Map of slot name → item uid
 * @param getItemDefById Function to resolve an item uid to its ItemDef (via inventory lookup)
 * @param getItemBaseRank Function to resolve an item uid to its BaseRank string
 * @param getPotentialLines Function to resolve an item uid to its PotentialLine array
 */
export interface EquippedBonuses {
  atk: number;
  str: number;
  dex: number;
  int: number;
  luk: number;
  wDef: number;
  mDef: number;
  speed: number;
  jump: number;
  hp: number;
  mp: number;
}

export function resolveEquippedBonus(
  equipped: Map<string, string> | Record<string, string> | undefined,
  getItemDefById: (uid: string) => ItemDef | undefined,
  getItemBaseRank: (uid: string) => BaseRank,
  getPotentialLines: (uid: string) => readonly PotentialLine[],
  getBonusStats?: (uid: string) => readonly BonusStatLine[],
): EquippedBonuses {
  const result: EquippedBonuses = {
    atk: 0,
    str: 0,
    dex: 0,
    int: 0,
    luk: 0,
    wDef: 0,
    mDef: 0,
    speed: 0,
    jump: 0,
    hp: 0,
    mp: 0,
  };
  if (!equipped) return result;

  const entries = equipped instanceof Map ? equipped.entries() : Object.entries(equipped);
  for (const [_slot, uid] of entries) {
    const def = getItemDefById(uid);
    if (!def) continue;
    const rank = getItemBaseRank(uid);
    const mult = getBaseRankInfo(rank).statMultiplier;

    // Weapon baseAttack contributes to ATK bonus.
    result.atk += def.baseAttack * mult;

    // Primary stat bonus scaled by rank.
    const statKey = def.primaryStat.toLowerCase() as keyof Omit<
      EquippedBonuses,
      "atk" | "hp" | "mp"
    >;
    result[statKey] += def.baseStatBonus * mult;

    // Flat defence / utility stats from item definition (not rank-scaled).
    result.wDef += def.wDef ?? 0;
    result.mDef += def.mDef ?? 0;
    result.speed += def.speed ?? 0;
    result.jump += def.jump ?? 0;

    // Flat HP/MP bonuses from item definition.
    result.hp += def.hpBonus ?? 0;
    result.mp += def.mpBonus ?? 0;

    // Potential lines add percentage bonuses (based on primaryStatBonus, so ATK + STR/DEX/INT/LUK only).
    for (const line of getPotentialLines(uid)) {
      if (line.stat === "HP" || line.stat === "MP") continue; // HP/MP percentages use a different base — not yet supported
      const bonus = Math.floor((line.percent / 100) * def.baseStatBonus * mult);
      if (line.stat === "ATK") {
        result.atk += bonus;
      } else {
        const key = line.stat.toLowerCase() as keyof Omit<EquippedBonuses, "atk" | "hp" | "mp">;
        if (key in result) result[key] += bonus;
      }
    }

    // Bonus stats (flame) add flat bonuses.
    if (getBonusStats) {
      const FLAME_STAT_MAP: Record<string, keyof EquippedBonuses> = {
        ATK: "atk",
        STR: "str",
        DEX: "dex",
        INT: "int",
        LUK: "luk",
        WDEF: "wDef",
        MDEF: "mDef",
        SPEED: "speed",
        JUMP: "jump",
        HP: "hp",
        MP: "mp",
      };
      for (const bs of getBonusStats(uid)) {
        const key = FLAME_STAT_MAP[bs.stat];
        if (key) result[key] += bs.value;
      }
    }
  }
  return result;
}

/** Number of bonus stat lines an instance should roll for its tier. */
export function lineCountForTier(tier: PotentialTier): number {
  return getPotentialTierInfo(tier).lines;
}

/** Possible stats a potential line can roll, plus ATK for weapon-only lines. */
const POTENTIAL_STATS: readonly (PrimaryStat | "HP" | "MP" | "ATK")[] = [
  "STR",
  "DEX",
  "INT",
  "LUK",
  "HP",
  "MP",
  "ATK",
];

/** Roll a single random potential line. Percent bonus ranges from 1% to 12%. */
export function rollPotentialLine(rng: () => number = Math.random): PotentialLine {
  const stat = POTENTIAL_STATS[Math.floor(rng() * POTENTIAL_STATS.length)];
  if (stat === undefined) {
    throw new Error("rollPotentialLine: POTENTIAL_STATS is empty");
  }
  const percent = Math.floor(rng() * 12) + 1; // 1–12%
  return { stat, percent };
}

/** Generate all potential lines for a given tier. */
export function rollPotentialLines(
  tier: PotentialTier,
  rng: () => number = Math.random,
): PotentialLine[] {
  const count = lineCountForTier(tier);
  const lines: PotentialLine[] = [];
  for (let i = 0; i < count; i++) {
    lines.push(rollPotentialLine(rng));
  }
  return lines;
}

